const pow = (x, n) => {
  let result = 1;

  for (let i = 0; i < n; i++) {
    result *= x;
  }

  return result;
}

log(pow(2, 3)); // 8 

const pow1 = (x, n) => {
  if (n === 1) {
    return x;
  } else {
    return x * pow(x, n - 1);
  }
  
}

log(pow1(2, 3)); // 8 

