const sum = (n) => {
  let sum = 0;

  for (let i = 0; i <= n; i++) {
    sum += i;
  }

  return sum; // 15
}

log(sum(5)); // 1 + 2 + 3 + 4 + 5 = 15

const sum1 = (n) => {
  if (n === 1) return 1;
  return n + sum(n - 1);
}

log(sum1(5)); // 1 + 2 + 3 + 4 + 5 = 15

const sum3 = (n) => {
  return n * (n + 1) / 2;
}

log(sum3(5)); // 1 + 2 + 3 + 4 + 5 = 15

